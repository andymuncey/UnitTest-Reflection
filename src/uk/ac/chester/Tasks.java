public class Tasks {

    /**
     * sums the values of all integers in an array
     * @param numbers
     * @return the sum of all integers in the array
     */
    int arraySum(int[] numbers){
        int total = 0;
        for (double number: numbers)
        {
            total += number;
        }
        return total;
    }


}
